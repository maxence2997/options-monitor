"""
策略常數設定檔
═══════════════════════════════════════════════════════════════════
所有影響「何時發通知、何時停損、何時獲利了結」的門檻值都集中在這裡。
想調整策略參數時，只需修改此檔，不需要進 strategy.py 翻程式碼。
═══════════════════════════════════════════════════════════════════
"""

# ── 獲利了結門檻（全域預設值）────────────────────────────────────────
# 當持倉的 P&L% 達到此值時，系統發送「建議平倉」通知。
# 賣方策略（CSP / CC / IC）的邏輯：
#   開倉收 premium $300，目前只需 $150 買回 → 已獲利 50% → 平倉鎖利、進下一輪
# 提早平倉的優點：
#   - 鎖定大部分獲利，不再承擔剩餘風險
#   - 釋放保證金，更快開下一輪收 premium
DEFAULT_PROFIT_TARGET_PCT: float = 50.0

# ── 停損門檻（全域預設值）────────────────────────────────────────────
# 當持倉虧損達到「開倉收到 premium 的 N 倍」時，系統發送「停損」通知。
# 例：開倉收 $300，虧損達到 $600（即 200%）→ 觸發停損
# 設定邏輯：虧損不超過收益的 2 倍，期望值依然為正
DEFAULT_LOSS_LIMIT_PCT: float = 200.0

# ── 各策略個別獲利目標（覆蓋全域預設值）─────────────────────────────
# 買方策略（BULL_CALL_SPREAD / HEDGE_PUT）目標設高一點，
# 因為這類策略是用小錢賭大行情，要讓獲利足夠大才值得。
STRATEGY_PROFIT_TARGETS: dict[str, float] = {
    "WHEEL_CSP":        50.0,   # 賣 Put：收到 50% premium 就走
    "WHEEL_CC":         50.0,   # 賣 Call：同上
    "IRON_CONDOR":      50.0,   # IC：收到 50% premium 就平倉整組
    "BULL_CALL_SPREAD": 100.0,  # 買 Call Spread：要翻倍才平倉（賭方向）
    "HEDGE_PUT":        200.0,  # 對沖 Put：要漲很多才值得，否則當保費看待
}

# ── 各策略個別停損門檻（覆蓋全域預設值）─────────────────────────────
# NVDA 波動大，CSP 的停損門檻放寬到 300%，
# 避免正常波動就觸發停損後，股票反彈卻已出場。
# 買方策略（BULL_CALL_SPREAD / HEDGE_PUT）最多虧掉 premium 本身（100%），
# 不需要再設更寬的門檻。
STRATEGY_LOSS_LIMITS: dict[str, float] = {
    "WHEEL_CSP":        300.0,  # NVDA 波動大，給更多空間
    "WHEEL_CC":         200.0,  # 一般賣方門檻
    "IRON_CONDOR":      200.0,  # Spread 有保護腳，虧損已有限，200% 夠了
    "BULL_CALL_SPREAD": 100.0,  # 買方：虧光 premium 就停，不會更多
    "HEDGE_PUT":        100.0,  # 對沖保險：到期歸零就歸零，不需要停損
}

# ── 到期日警告門檻 ────────────────────────────────────────────────────
# DTE（Days To Expiration）≤ 此值時，發送「快到期」提醒。
# 原因：期權最後幾天的 Gamma 風險急劇上升，
#       小幅股價波動就可能讓 OTM 變 ITM，建議提早處理。
DTE_WARNING_DAYS: int = 7

# ── Assignment 風險警告門檻 ───────────────────────────────────────────
# 當股價距離 Strike 的距離 ≤ 此百分比時，發送「Assignment 風險」警告。
# 例：NVDA $180，CSP Strike $175，距離 = (180-175)/180 = 2.8% → 觸發警告
# 設定 5% 的原因：給自己足夠的反應時間，
#                 在真正被 Assign 前決定是否 Roll 展期。
ASSIGNMENT_RISK_PCT: float = 5.0
